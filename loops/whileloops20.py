###make an algorithm that takes 2 numbers as inputs from the user.
###then we will multiply these numbers and print the result.
###eg - if input is 5 and 4, 20 should be printed.
###note - don't use multiply operator, you can user +,-

n1=int(input("enter first number"))
n2=int(input("enter second number"))
sum=0
i=1
while i<n2+1:
    sum=sum+n1
    i-=-1
print(sum)